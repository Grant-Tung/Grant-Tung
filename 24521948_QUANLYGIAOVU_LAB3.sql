    SELECT AVG(DIEM) FROM KETQUATHI K WHERE K.MAHV = HOCVIEN.MAHV 
    AND K.LANTHI = (SELECT MAX(LANTHI) FROM KETQUATHI WHERE MAHV = K.MAHV AND MAMH = K.MAMH)
