-- Cau 4
UPDATE HOCVIEN SET XEPLOAI = CASE 
    WHEN DIEMTB >= 9 THEN N'Xuất sắc' 
    WHEN DIEMTB >= 8 THEN N'Giỏi' 
    WHEN DIEMTB >= 6.5 THEN N'Khá' 
    WHEN DIEMTB >= 5 THEN N'Trung bình' 
    ELSE N'Yếu' END
