WHERE KQ.MAMH = 'CSDL' AND KQ.LANTHI = 1 AND KQ.KQUA = 'Khong Dat' 
AND HV.MAHV NOT IN (SELECT MAHV FROM KETQUATHI WHERE MAMH = 'CSDL' AND LANTHI > 1)

-- Cau 13
SELECT GV.MAGV, GV.HOTEN FROM GIAOVIEN GV WHERE GV.MAGV NOT IN (SELECT DISTINCT GD.MAGV FROM GIANGDAY GD)

-- Cau 14
SELECT GV.MAGV, GV.HOTEN FROM GIAOVIEN GV WHERE NOT EXISTS (
    SELECT * FROM GIANGDAY GD JOIN MONHOC MH ON GD.MAMH = MH.MAMH 
    WHERE GD.MAGV = GV.MAGV AND MH.MAKHOA = GV.MAKHOA
)

-- Cau 15
SELECT DISTINCT HV.HO, HV.TEN FROM HOCVIEN HV JOIN KETQUATHI KQ ON HV.MAHV = KQ.MAHV 
WHERE HV.MALOP = 'K11' AND ((KQ.LANTHI = 3 AND KQ.KQUA = 'Khong Dat') OR (KQ.MAMH = 'CTRR' AND KQ.LANTHI = 2 AND KQ.DIEM = 5))

-- Cau 16
SELECT GV.HOTEN, GD.HOCKY, GD.NAM FROM GIAOVIEN GV JOIN GIANGDAY GD ON GV.MAGV = GD.MAGV 
WHERE GD.MAMH = 'CTRR' GROUP BY GV.MAGV, GV.HOTEN, GD.HOCKY, GD.NAM HAVING COUNT(DISTINCT GD.MALOP) >= 2

-- Cau 17
SELECT HV.MAHV, HV.HO, HV.TEN, KQ.DIEM FROM HOCVIEN HV JOIN KETQUATHI KQ ON HV.MAHV = KQ.MAHV 
WHERE KQ.MAMH = 'CSDL' AND KQ.LANTHI = (SELECT MAX(LANTHI) FROM KETQUATHI WHERE MAHV = HV.MAHV AND MAMH = 'CSDL')

-- Cau 18
SELECT HV.MAHV, HV.HO, HV.TEN, MAX(KQ.DIEM) AS DIEM_CAO_NHAT FROM HOCVIEN HV 
JOIN KETQUATHI KQ ON HV.MAHV = KQ.MAHV JOIN MONHOC MH ON KQ.MAMH = MH.MAMH 
WHERE MH.TENMH = N'Cơ sở dữ liệu' GROUP BY HV.MAHV, HV.HO, HV.TEN
